# some formate specifiers

# %s	String	printf("Hello, %s!\n", "World") → Hello, World!
# %d	Integer (Decimal)	printf("Age: %d\n", 25) → Age: 25
# %i	Integer (Same as %d)	printf("Number: %i\n", 42) → Number: 42
# %f	Floating point (Default: 6 decimal places)	printf("Pi: %f\n", 3.14159) → Pi: 3.141590
# %.nf	Floating point with n decimal places	printf("Pi: %.2f\n", 3.14159) → Pi: 3.14
# %e	Scientific notation (lowercase)	printf("Sci: %e\n", 1234.5678) → Sci: 1.234568e+03
# %E	Scientific notation (uppercase)	printf("Sci: %E\n", 1234.5678) → Sci: 1.234568E+03
# %x	Lowercase hexadecimal	printf("Hex: %x\n", 255) → Hex: ff
# %X	Uppercase hexadecimal	printf("Hex: %X\n", 255) → Hex: FF
# %o	Octal number	printf("Octal: %o\n", 64) → Octal: 100
# %c	Character (from ASCII code)	printf("Char: %c\n", 65) → Char: A
# %b	Binary number	printf("Binary: %b\n", 5) → Binary: 101
# %%	Literal % sign	printf("Percent: %%\n") → Percent: %


