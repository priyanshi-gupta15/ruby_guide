#if statement

age = 18
if age >= 18
  puts "You are an adult."
elsif age >= 13
  puts "You are a teenager."
else
  puts "You are a child."
end


# Ternary Operator
puts age >= 18 ? "Adult" : "Minor"


# Case Statement {switch case}
grade = "A"
case grade
when "A"
  puts "Excellent!"
when "B"
  puts "Good job!"
else
  puts "Keep trying!"
end

